// Nombre de la clase
class Multiplicacion {
    // declaracion de variables
    int a, b;

    // metodo constructor de la clase
    Multiplicacion() {
    }
    // get de la variable a
    public int getA() {
        return a;
    }
    // set de la variable a
    public void setA(int a) {
        this.a = a;
    }
    // get de la variable b
    public int getB() {
        return b;
    }
    // set de la variable b
    public void setB(int b) {
        this.b = b;
    }

}